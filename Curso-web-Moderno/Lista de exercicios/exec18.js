const numberExtense = number => {
    switch(number){
        case 0:
            console.log('Zero')
            break
        case 1:
            console.log('Um')
            break
        case 2:
            console.log('Dois')
            break
        case 3:
            console.log('Três')
            break
        case 4:
            console.log('Quatro')
            break
        case 5:
            console.log('Cinco')
            break
        case 6:
            console.log('Seis')
            break
        case 7:
            console.log('Sete')
            break
        case 8:
            console.log('Oito')
            break
        case 9:
            console.log('Nove')
            break
        case 10:
            console.log('Dez')
            break
        default:
            console.log('Número inválido')
    }
}

numberExtense(0)
numberExtense(1)
numberExtense(2)
numberExtense(3)
numberExtense(4)
numberExtense(5)
numberExtense(6)
numberExtense(7)
numberExtense(8)
numberExtense(9)
numberExtense(10)
